package com.for_loop;

public class Asignment6_Q1 {

	public static void main(String[] args) {
	// Write a program to calculate the sum of the first 10 natural numbers.
		 int n = 10;
	     int sum = 0;

	     for (int i = 1; i <= n; i++) {
	    	 sum += i;
	     }

	     System.out.println("Sum of the first  10  natural numbers is: " + sum);
	    
	}
}


